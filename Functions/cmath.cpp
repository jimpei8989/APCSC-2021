#include <cmath>
#include <iostream>

int main() {
    std::cout << "sqrt(4): " << sqrt(4) << std::endl;
    std::cout << "sqrt(123.456): " << sqrt(123.456) << std::endl;

    std::cout << "pow(2, 3): " << pow(2, 3) << std::endl;
    std::cout << "pow(2.1, 7.89): " << pow(2.1, 7.89) << std::endl;
}
